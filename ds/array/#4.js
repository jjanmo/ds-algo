function solution2(nums) {
  let left = 0;

  for (let i = 0; i < nums.length; i++) {
    let right = 0;
    for (let j = i + 1; j < nums.length; j++) {
      right += nums[j];
    }

    if (left === right) return i;
    left += nums[i];
  }

  return -1;
}

