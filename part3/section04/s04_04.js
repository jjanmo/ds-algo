// → 상품의 합(가격+배송비)이 큰 순서대로만 정렬한 후 이를 가지고 더하기 때문에 할인가능한 상품은 총 합이 예산을 넘어가는 시점 이 후부터 할인의 경우의 수를 고려하게 된다.
// → 하지만 할인 상품이 총 합이 예산을 넘어가는 시점에만 나타나는 것은 아니다. 상품가격만 비싸고 배송비가 싼 경우가 할인되는 경우 예외 상황이 발생할 수 있다.(예시 3)
