function solution1(total, list) {
  list.sort((a, b) => a[0] + a[1] - (b[0] + b[1]));

  let sum = 0;
  let count = 0;
  let index = 0;
  for (let i = 0; i < list.length; i++) {
    sum += list[i][0] + list[i][1];
    count++;
    if (sum > total) {
      sum -= list[i][0] + list[i][1];
      count--;
      index = i;
      break;
    }
  }

  // 할인받는 물건 체크
  for (let i = index; i < list.length; i++) {
    const value = list[i][0] / 2;
    sum += value + list[i][1];
    if (sum <= total) {
      count++;
    } else {
      sum -= value + list[i][1];
    }
  }

  return count;
}
