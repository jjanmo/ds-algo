function solution1(n, data) {
  data.sort((a, b) => a[0] + a[1] - (b[0] + b[1]));

  let count = 0;
  for (let i = 0; i < data.length; i++) {
    const [document, interview] = data[i];
    let passed = true;
    for (let j = i - 1; j >= 0; j--) {
      if (document > data[j][0] && interview > data[j][1]) {
        passed = false;
        break;
      }
    }
    if (passed) count++;
  }

  return count;
}

console.log(
  solution1(5, [
    [3, 2],
    [1, 4],
    [4, 1],
    [2, 3],
    [5, 5],
  ])
); // 4
console.log(
  solution1(7, [
    [3, 6],
    [7, 3],
    [4, 2],
    [1, 4],
    [5, 7],
    [2, 5],
    [6, 1],
  ])
); // 3

