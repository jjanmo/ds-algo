function solution2(arr1, arr2) {
  let p1 = 0,
    p2 = 0;

  arr1.sort((a, b) => a - b);
  arr2.sort((a, b) => a - b);

  const result = [];
  while (p1 !== arr2.length && p2 !== arr2.length) {
    if (arr1[p1] === arr2[p2]) {
      result.push(arr2[p2]);
      p1++;
      p2++;
    } else if (arr1[p1] > arr2[p2]) {
      p2++;
    } else {
      p1++;
    }
  }

  return result;
}

